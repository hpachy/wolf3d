#include <unistd.h>
#include "libft.h"

void	ft_putstr(const char *s)
{
	int	a;

	a = 0;
	if (s)
	{
		while (s[a] != '\0')
		{
			ft_putchar(s[a]);
			a++;
		}
	}
	if (!(s))
		ft_putchar('\0');
}
