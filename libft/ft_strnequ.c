#include <string.h>
#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	if (s1 && s2)
		return (ft_strncmp((char *)s1, (char *)s2, (int)n) ? 0 : 1);
	return (0);
}
