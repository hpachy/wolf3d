int		ft_calcul(long int c)
{
	int	nb_division;

	nb_division = 0;
	if (c <= 0)
	{
		nb_division++;
		c = c * -1;
	}
	while (c > 0)
	{
		nb_division++;
		c = c / 10;
	}
	return (nb_division);
}
