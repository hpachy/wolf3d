char		*ft_isspace(char *c)
{
	int i;

	i = 0;
	while (c[i] == '\n' || c[i] == '\t' || c[i] == ' ' || c[i] == '\v' ||
		c[i] == '\r' || c[i] == '\f')
	{
		i++;
	}
	return (c + i);
}
